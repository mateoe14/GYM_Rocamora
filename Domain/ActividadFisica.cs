public class ActividadFisica
{
    //ATRIBUTOS
    private String nombre {get; set;};
    private int cantHorasSem {get; set;};

    //CONSTRUCTOR
    public ActividadFisica(){}

    public ActividadFisica(String pNombre, int pCantHorasSem)
    {
        this.nombre = pNombre;
        this.cantHorasSem = pCantHorasSem;
    }




}